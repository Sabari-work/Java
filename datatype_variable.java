class datatype_variable{
    public static void main(String args[]){
        int a=67;                              //a is variable,int is datatype
        String john= "J";                      //john is a variable,string is datatype & S should be capital,words should be double quoted
        String b=john;
        System.out.println(a);                 //ln for next line
        System.out.print(b);
    }
}